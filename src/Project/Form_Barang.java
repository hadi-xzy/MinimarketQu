    private void btnubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnubahActionPerformed
        // TODO add your handling code here:
        try{
            String sql ="UPDATE barang SET nama_barang='"+txtnama.getText()
                    +"',satuan ='"+cbsatuan.getSelectedItem()
                    +"',harga='"+txtharga.getText()
                    +"',stok='"+txtstok.getText()
                    +"'WHERE kd_barang='"+txtkd.getText()+"'";
            java.sql.Connection con = (Connection)koneksi.configDB();
            java.sql.PreparedStatement pstm = con.prepareStatement(sql);
            pstm.execute();
            
            JOptionPane.showMessageDialog(null, "Edit Data Berhasil");
            
            tampilkan_data();
            kosongkan_form();
        }catch (HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        btnsimpan.setEnabled(true);
        btnubah.setEnabled(false);
        btnhapus.setEnabled(false);
    }//GEN-LAST:event_btnubahActionPerformed

