    private void tabelbarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelbarangMouseClicked
        // TODO add your handling code here:
        JOptionPane.showMessageDialog(null, 
             "Silahkan data diperbaharui terlebih dahulu \n Lakukan dengan benar \n",
             "Perbaharui Data !",JOptionPane.PLAIN_MESSAGE);
             btnsimpan.setEnabled(false);
             btnubah.setEnabled(true);
             btnhapus.setEnabled(true);
        int baris =tabelbarang.rowAtPoint(evt.getPoint());
        String kd_barang =tabelbarang.getValueAt(baris, 1).toString();
        txtkd.setText(kd_barang);
        String nama_barang=tabelbarang.getValueAt(baris, 2).toString();
        txtnama.setText(nama_barang);
        String satuan=tabelbarang.getValueAt(baris, 3).toString();
        cbsatuan.setSelectedItem(satuan);
        String harga=tabelbarang.getValueAt(baris, 4).toString();
        txtharga.setText(harga);
        String stok=tabelbarang.getValueAt(baris, 5).toString();
        txtstok.setText(stok);
    }//GEN-LAST:event_tabelbarangMouseClicked

