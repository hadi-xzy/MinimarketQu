    private void tabeluserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabeluserMouseClicked
        JOptionPane.showMessageDialog(null, 
             "Silahkan data diperbaharui terlebih dahulu \n Lakukan dengan benar \n",
             "Perbaharui Data !",JOptionPane.PLAIN_MESSAGE);
             btnsimpan.setEnabled(false);
             btnubah.setEnabled(true);
             btnhapus.setEnabled(true);
        int baris =tabeluser.rowAtPoint(evt.getPoint());
        String id_user =tabeluser.getValueAt(baris, 1).toString();
        txtid.setText(id_user);
        String nama=tabeluser.getValueAt(baris, 2).toString();
        txtnama.setText(nama);
        String alamat=tabeluser.getValueAt(baris, 3).toString();
        txtalamat.setText(alamat);
        String telpon=tabeluser.getValueAt(baris, 4).toString();
        txttlp.setText(telpon);
        String status=tabeluser.getValueAt(baris, 5).toString();
        cbstatus.setSelectedItem(status);
        String username=tabeluser.getValueAt(baris, 6).toString();
        txtuser.setText(username);
        String password=tabeluser.getValueAt(baris, 7).toString();
        txtpw.setText(password);
        String akses=tabeluser.getValueAt(baris, 8).toString();
        txtacc.setText(akses);
    }//GEN-LAST:event_tabeluserMouseClicked

