    private void tampilkan_data(){
        //membuat judul kolom pada TablePenjualan
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NO");
        model.addColumn("Id User");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("Telepon");
        model.addColumn("Status");
        model.addColumn("Username");
        model.addColumn("Password");
        model.addColumn("Akses");
        
        //variabel cari untuk proses pencarian data pada txtcaridata
        String cari = txtcari.getText();
        try{
            int no=1;
            String sql ="SELECT * FROM user WHERE id_user LIKE '%"+cari+"%'";
            //panggil class koneksi
            java.sql.Connection con = (Connection)koneksi.configDB();
            java.sql.Statement stm = con.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while (res.next()){
                model.addRow(new Object[]{no++,res.getString(1),
                    res.getString(2),res.getString(3),res.getString(4),
                    res.getString(5),res.getString(6),res.getString(7),
                    res.getString(8)});
                tabeluser.setModel(model);
            }
        }catch (SQLException e){
            //tampilkan pesan di sistem kalau terjadi error
            System.out.println("Error :"+e.getMessage());
        }
    }

